from aoc25.seven.solution import TachyonParser


def test_part_one_example():
    parser = TachyonParser()
    example = """.......S.......
...............
.......^.......
...............
......^.^......
...............
.....^.^.^.....
...............
....^.^...^....
...............
...^.^...^.^...
...............
..^...^.....^..
...............
.^.^.^.^.^...^.
..............."""
    field = parser.parse(example)
    assert field.solve() == 21


